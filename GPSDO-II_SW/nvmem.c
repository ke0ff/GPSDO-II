	// Only first element is populated here.  This allows for 11 updates before an erase is needed
	// Up to 5 PLL frames are possible (this is how many are needed to init the UX-129)
	//	High bit set = end of PLL frames.  High bit set of first frame = last empty element.
	//	VOL, SQU, BAND, and MODULE ID are packed into a single 32b word:
	//		[bbiissvv]
	//		bb = discretes:
	//			[xxxx|xxHB]
	//			H = high power
	//			B = BAND
	//		ii = alt squelch (P0.3 = 0) (0x00 = max closed, 0xff = max open)
	//		ss = squelch (P0.3 = 1)
	//		vv = vol  (0xff is min, 0x00 is max)
	//	PLL field is llpppppp:
	//		ll = length of serial XFR (1 to 24)
	//		pppppp = up to 24 bits of serial data (transferred msb first)
	//		RX frames are first 5 words, TX frames are 2nd set of 5 words

